// ─────────────────────────────────────────────────────────────────────
// Pure content generators. No I/O. Easy to unit-test later.
// All paths use Shopify's `template_suffix=machine` convention.
// ─────────────────────────────────────────────────────────────────────

const MACHINE_PARAM = "?view=machine";

const stripHtml = (s: string): string =>
  s
    .replace(/<[^>]+>/g, " ")
    .replace(/\s+/g, " ")
    .trim();

const truncate = (s: string, n: number): string =>
  s.length <= n ? s : `${s.slice(0, n - 1).trimEnd()}…`;

const productPath = (p: ProductSummary) => `/products/${p.handle}`;
const collectionPath = (c: CollectionSummary) => `/collections/${c.handle}`;
const pagePath = (p: PageSummary) => `/pages/${p.handle}`;
const articlePath = (a: ArticleSummary) =>
  `/blogs/${a.blogHandle}/${a.handle}`;

// ─── llms.txt — the index ────────────────────────────────────────────

export function generateLlmsTxt(args: {
  shop: ShopInfo;
  products: ProductSummary[];
  collections: CollectionSummary[];
  pages: PageSummary[];
  articles: ArticleSummary[];
}): string {
  const { shop, products, collections, pages, articles } = args;
  const lines: string[] = [];

  lines.push(`# ${shop.name}`);
  lines.push("");
  if (shop.description) {
    lines.push(`> ${shop.description.replace(/\s+/g, " ").trim()}`);
    lines.push("");
  }
  lines.push(`Storefront: ${shop.url}`);
  lines.push("");

  if (products.length) {
    lines.push("## Products");
    for (const p of products) {
      const summary = truncate(stripHtml(p.description) || p.productType || "", 140);
      const price =
        p.priceRangeFrom && p.currencyCode
          ? ` — ${p.currencyCode} ${p.priceRangeFrom}`
          : "";
      lines.push(`- [${p.title}](${productPath(p)}${MACHINE_PARAM})${price}${summary ? `: ${summary}` : ""}`);
    }
    lines.push("");
  }

  if (collections.length) {
    lines.push("## Collections");
    for (const c of collections) {
      const summary = truncate(stripHtml(c.description), 140);
      lines.push(
        `- [${c.title}](${collectionPath(c)}${MACHINE_PARAM})${summary ? `: ${summary}` : ""}`,
      );
    }
    lines.push("");
  }

  if (pages.length) {
    lines.push("## Pages");
    for (const p of pages) {
      const summary = truncate(stripHtml(p.bodySummary), 140);
      lines.push(
        `- [${p.title}](${pagePath(p)}${MACHINE_PARAM})${summary ? `: ${summary}` : ""}`,
      );
    }
    lines.push("");
  }

  if (articles.length) {
    lines.push("## Articles");
    for (const a of articles) {
      const summary = truncate(stripHtml(a.summary), 140);
      lines.push(
        `- [${a.title}](${articlePath(a)}${MACHINE_PARAM})${summary ? `: ${summary}` : ""}`,
      );
    }
    lines.push("");
  }

  lines.push("---");
  lines.push("");
  lines.push(
    "_This page is the AI-readable index for this store. Generated by AutoAEO._",
  );
  lines.push(`_Last updated: ${new Date().toISOString()}_`);

  return lines.join("\n");
}

// ─── llms-full.txt — concatenated full corpus ────────────────────────

export function generateLlmsFullTxt(args: {
  shop: ShopInfo;
  products: ProductSummary[];
  collections: CollectionSummary[];
  pages: PageSummary[];
  articles: ArticleSummary[];
}): string {
  const { shop, products, collections, pages, articles } = args;
  const out: string[] = [];

  out.push(`# ${shop.name}`);
  out.push("");
  if (shop.description) out.push(shop.description);
  out.push("");
  out.push(`Storefront: ${shop.url}`);
  out.push("");

  if (products.length) {
    out.push("# Products");
    out.push("");
    for (const p of products) {
      out.push(`## ${p.title}`);
      out.push("");
      const meta: string[] = [];
      if (p.priceRangeFrom && p.currencyCode) {
        const price =
          p.priceRangeFrom === p.priceRangeTo
            ? `${p.currencyCode} ${p.priceRangeFrom}`
            : `${p.currencyCode} ${p.priceRangeFrom}–${p.priceRangeTo}`;
        meta.push(`**Price:** ${price}`);
      }
      if (p.vendor) meta.push(`**Vendor:** ${p.vendor}`);
      if (p.productType) meta.push(`**Type:** ${p.productType}`);
      if (p.tags.length) meta.push(`**Tags:** ${p.tags.join(", ")}`);
      meta.push(`**URL:** ${shop.url}${productPath(p)}`);
      out.push(meta.join("  \n"));
      out.push("");
      const desc = stripHtml(p.description);
      if (desc) {
        out.push(desc);
        out.push("");
      }
      out.push("---");
      out.push("");
    }
  }

  if (collections.length) {
    out.push("# Collections");
    out.push("");
    for (const c of collections) {
      out.push(`## ${c.title}`);
      out.push("");
      out.push(`**URL:** ${shop.url}${collectionPath(c)}`);
      if (c.productsCount != null) {
        out.push(`**Products:** ${c.productsCount}`);
      }
      out.push("");
      const desc = stripHtml(c.description);
      if (desc) {
        out.push(desc);
        out.push("");
      }
      out.push("---");
      out.push("");
    }
  }

  if (pages.length) {
    out.push("# Pages");
    out.push("");
    for (const p of pages) {
      out.push(`## ${p.title}`);
      out.push("");
      out.push(`**URL:** ${shop.url}${pagePath(p)}`);
      out.push("");
      const body = stripHtml(p.bodySummary);
      if (body) {
        out.push(body);
        out.push("");
      }
      out.push("---");
      out.push("");
    }
  }

  if (articles.length) {
    out.push("# Articles");
    out.push("");
    for (const a of articles) {
      out.push(`## ${a.title}`);
      out.push("");
      out.push(`**URL:** ${shop.url}${articlePath(a)}`);
      if (a.publishedAt) {
        out.push(`**Published:** ${a.publishedAt}`);
      }
      out.push("");
      const body = stripHtml(a.summary);
      if (body) {
        out.push(body);
        out.push("");
      }
      out.push("---");
      out.push("");
    }
  }

  return out.join("\n");
}

