// ─── JSON templates that wire the sections in via template_suffix=machine ─

export function machineTemplateJson(sectionType: string): string {
  return JSON.stringify(
    {
      sections: {
        main: { type: sectionType, settings: {} },
      },
      order: ["main"],
    },
    null,
    2,
  );
}

